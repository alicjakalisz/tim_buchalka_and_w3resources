package alicja.examples.book.arrays;

public class EnhancedLoopArray {

    public static void main(String[] args) {



        String[] names ={"Basia", "Kasia", "Asia","Zosia"};

        //enhanced loop to print out the elements of the array

        for (String name : names){
            System.out.println(name);

        }


    }


}
